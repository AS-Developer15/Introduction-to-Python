# Var Decalartion
a, b = 10, 3
#  + is for addition
print(a + b)
#  - is for subtraction
print(a - b)
#  * is for multiplication
print(a * b)
#  ** is for exponential growth
print(a**b)
#  / is for division
print(a / b)
#  % is for modulus i.e finding remainder
print(a % b)
# // os for floor division i.e round off the val
print(a // b)
